function loadJsonIfExists<T>(filename: string): Record<string, T> | null {
  const p = join(dataDir, filename);
  if (!existsSync(p)) return null;
  return JSON.parse(readFileSync(p, 'utf8'));
}

// Editable JSON overrides. If a file is missing, fall back to @pkmn/dex directly.
const speciesOverrides = loadJsonIfExists<any>('species.json');
const movesOverrides = loadJsonIfExists<any>('moves.json');
const itemsOverrides = loadJsonIfExists<any>('items.json');
const abilitiesOverrides = loadJsonIfExists<any>('abilities.json');
const naturesOverrides = loadJsonIfExists<any>('natures.json');
// learnsets.json is `{ speciesId: [moveId, ...] }`. Generated by refresh-data
// because @pkmn/dex.learnsets is async, which doesn't compose with our sync
// data accessors elsewhere.
const learnsetsData = loadJsonIfExists<string[]>('learnsets.json');

const gen = Dex.forGen(9);

function toId(name: string): string {
  return name.toLowerCase().replace(/[^a-z0-9]/g, '');
}

export function getSpecies(name: string) {
  const id = toId(name);
  return speciesOverrides?.[id] ?? gen.species.get(name);
}
export function getMove(name: string) {
  const id = toId(name);
  return movesOverrides?.[id] ?? gen.moves.get(name);
}

// True for moves that charge turn 1 + fire turn 2 (Solar Beam, Electro
// Shot, Sky Attack, Phantom Force, Fly, Dig, Bounce, Meteor Beam, etc.).
// The dex marks these with flags.charge === 1. Power Herb / sun (Solar
// Beam) / electric terrain (Electro Shot) can collapse it to one turn —
// when that happens the user just logs damage normally and the charging
// state never gets set.
export function isChargeMove(name: string): boolean {
  const m = getMove(name) as any;
  return !!(m?.flags?.charge);
}

// True for "pivot" moves that switch the attacker out after resolving
// (U-turn, Volt Switch, Flip Turn, Parting Shot, Teleport, Chilly
// Reception, Baton Pass, Shed Tail). The dex marks these via the
// `selfSwitch` field — `true` for the basic forms, `'copyvolatile'` for
// Baton Pass, `'shedtail'` for Shed Tail. We treat all of them the same
// for prompt purposes (the user picks an incoming mon next).
export function isPivotMove(name: string): boolean {
  const m = getMove(name) as any;
  return !!m?.selfSwitch;
}
// Moves that remove (or consume) the target's held item on hit. Knock Off /
// Thief / Covet / Corrosive Gas strip any item; Incinerate / Bug Bite / Pluck
// destroy a held berry. We mark the target's item as gone for damage-calc
// purposes either way. Trick / Switcheroo SWAP items (not handled here — that
// needs to track the swapped-in item, a separate follow-up).
const ITEM_REMOVING_MOVES = new Set([
  'Knock Off', 'Thief', 'Covet', 'Corrosive Gas', 'Incinerate', 'Bug Bite', 'Pluck',
]);
export function isItemRemovingMove(name: string): boolean {
  return ITEM_REMOVING_MOVES.has(name);
}
