export function loadFormat(): ChampionsFormat {
  const p = join(dataDir, 'format.champions.json');
  if (!existsSync(p)) {
    return {
      level: 50,
      teamSize: 6,
      bringSize: 4,
      gameType: 'doubles',
      gimmick: 'mega',
      gimmickAllowancePerSide: 1,
      openTeamSheets: true,
      itemClause: true,
      speciesClause: true,
      legality: { allow: [], ban: [] },
      items: { allow: [], ban: [] },
      moves: { ban: [] },
    };
  }
  const raw = JSON.parse(readFileSync(p, 'utf8'));
  // strip __notes if present
  const { __notes: _ignored, ...rest } = raw;
  const fmt = rest as ChampionsFormat;
  // Backfill defaults for fields that may be missing in older format files.
  if (fmt.gimmickAllowancePerSide == null) fmt.gimmickAllowancePerSide = 1;
  return fmt;
}

// Register loadFormat with the gimmick registry so activeGimmick() can resolve
// the configured gimmick without circular imports.
_setFormatLoader(() => loadFormat());

// Returns true for formes that ONLY exist as in-battle transformations —
// Mega Evolution, Primal Reversion, Gigantamax, Tera, etc. These should
// never appear in a team or autocomplete: the user picks the base species,
// equips the appropriate stone/item, and the engine swaps the forme at
// runtime (see gimmicks/mega.ts resolveSpecies).
export function isGimmickForme(speciesIdOrName: string): boolean {
  const sp = getSpecies(speciesIdOrName) as any;
  if (!sp || !sp.exists) return false;
  const forme: string | undefined = sp.forme;
  if (!forme) return false;
  // Match-prefix list so 'Mega', 'Mega-X', 'Mega-Y' all hit.
  return /^(Mega|Primal|Gmax|Tera|Eternamax|Origin|Ultra|Ash)\b/i.test(forme);
}

export function isLegalSpecies(speciesId: string, format = loadFormat()): boolean {
  const id = toId(speciesId);
  if (format.legality.ban.map(toId).includes(id)) return false;
  if (format.legality.allow.length === 0) return true;
  const allow = format.legality.allow.map(toId);
  if (allow.includes(id)) return true;
  // Reject gimmick formes (Mega/Primal/Gmax/Tera/etc.) outright — they're
  // not pickable team members; they're battle transformations.
  if (isGimmickForme(speciesId)) return false;
  // Forme fallback: Rotom-Wash, Lycanroc-Midnight, etc. aren't enumerated
  // in the format file — we resolve them to their base species in the dex
  // and allow if the base is allowed. This covers every Gen 1-9 forme
  // automatically (Rotom-*, Lycanroc-*, Urshifu-*, Landorus-T, etc.).
  const sp = getSpecies(speciesId) as any;
  if (!sp || !sp.exists) return false;
  const base = sp.baseSpecies as string | undefined;
  if (!base) return false;
  const baseId = toId(base);
  if (baseId === id) return false; // already checked above
  return allow.includes(baseId);
}

export function isLegalItem(itemId: string, format = loadFormat()): boolean {
  const id = toId(itemId);
  if (format.items.ban.map(toId).includes(id)) return false;
  if (format.items.allow.length === 0) return true;
  return format.items.allow.map(toId).includes(id);
}

// Display-name move list a species can learn, alphabetical, with format bans
// filtered out. Empty array if the species isn't in the learnset dump.
export function getLearnset(speciesName: string, format = loadFormat()): string[] {
  const id = toId(speciesName);
  const ids = learnsetsData?.[id] ?? [];
  if (!ids.length) return [];
  const bannedIds = new Set(format.moves.ban.map(toId));
  const names: string[] = [];
  for (const moveId of ids) {
    if (bannedIds.has(moveId)) continue;
    const m = getMove(moveId) as any;
    if (m?.name) names.push(m.name);
  }
  names.sort((a, b) => a.localeCompare(b));
  return names;
}

